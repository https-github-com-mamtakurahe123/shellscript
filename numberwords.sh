#!/bin/bash -x
echo "Enter any number from 1 to 10";
read n;
if [ $n -eq 0 ]
then
	echo "Entered number is ZERO" $n;
elif [ $n -eq 1 ]
then
        echo "Entered number is ONE" $n;
elif [ $n -eq 2 ]
then
        echo "Entered number is TWO" $n;
elif [ $n -eq 3 ]
then
        echo "Entered number is THREE" $n;
elif [ $n -eq 4 ]
then
        echo "Entered number is FOUR" $n;
elif [ $n -eq 5 ]
then
        echo "Entered number is FIVE" $n;
elif [ $n -eq 6 ]
then
        echo "Entered number is SIX" $n;
elif [ $n -eq 7 ]
then
        echo "Entered number is SEVEN" $n;
elif [ $n -eq 8 ]
then
        echo "Entered number is EIGHT" $n;
elif [ $n -eq 9 ]
then
        echo "Entered number is NIGHT" $n;
else
	echo "Entered Number is TEN" $n;
fi










